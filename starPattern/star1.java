package starPattern;

public class star1 {
public static void main(String[] arg) {
        // System.out.println("VIshwajeet");
        int num = 4;
        for (int i = 0; i < num; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
